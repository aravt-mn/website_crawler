# load nextpage function
# --------------------------------------------------
def next_page(browser, pj_cnt):
    is_next_page = False
    cnt = 0
    while cnt < retry_count:
        sleep(1)
        browser, code_str = check_code(browser)
        inputElement = browser.find_element_by_id('checkCode')
        # set code
        inputElement.clear()
        inputElement.send_keys(code_str)
        inputElement.send_keys(Keys.ENTER)
        browser.find_element_by_id('next_dataBar').click()
        WebDriverWait(browser, 15).until(EC.presence_of_all_elements_located)
        sleep(1)
        pg_title = browser.find_element_by_id('title').text
        print("\t\tNext Page Title: " + pg_title)
        rows = browser.find_element_by_xpath('//*[@id="dataGrid"]')\
            .find_elements_by_tag_name('tr')
        pj_num = int(rows[1].find_elements_by_tag_name('td')[0].text)
        if pg_title == '检索结果' and pj_cnt + 1 == pj_num:
            is_next_page = True
            sleep(1)
            break
        cnt += 1
    return is_next_page



