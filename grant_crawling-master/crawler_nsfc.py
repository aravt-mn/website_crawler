def get_projects(browser, target_category, target_year):
    rows = browser.find_element_by_xpath('//*[@id="dataGrid"]')\
        .find_elements_by_tag_name('tr')
    json_rows = []
    for row in rows:
        cells = row.find_elements_by_tag_name('td')
        pj_id = cells[0].text
        pj_number = cells[1].text
        app_code = cells[2].text
        pj_name = cells[3].text
        pj_manager = cells[4].text
        institution = cells[5].text
        award_amount = cells[6].text
