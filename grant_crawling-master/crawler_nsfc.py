# orc function
# --------------------------------------------------
def check_code(browser):
    cnt = 0
    code_str = ''
    while cnt < retry_count:
        # find part of the page you want image of
        element = browser.find_element_by_id('img_checkcode')
        location = element.location
        size = element.size
        # saves screenshot of entire page
        png = browser.get_screenshot_as_png()
        # uses PIL library to open image in memory
        im = Image.open(BytesIO(png))
        left = location['x']
        top = location['y']
        right = location['x'] + size['width']
        bottom = location['y'] + size['height']
        # defines crop points
        im = im.crop((left, top, right, bottom))
        code_str = pytesseract.image_to_string(im)
        print("\tOCR Result: " + colored(code_str, 'red'))
        if len(code_str) == 4 and (code_str.islower() or code_str.isdigit()):
            break
        else:
            browser.find_element_by_id('img_checkcode').click()
            sleep(1)
        cnt += 1
    return browser, code_str

