def logger(text):
    now = datetime.now()
    date_time = now.strftime("%m/%d/%Y, %H:%M:%S")
    f = open("log_snfc.txt", "a+")
    f.write(date_time + '\t' + str(text) + '\n')
    f.close()
    return True

